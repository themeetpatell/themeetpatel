// Helper functions for blog management
export const getArticleBySlug = (slug) => {
  return blogArticles.find(article => article.slug === slug);
};

export const getArticleById = (id) => {
  return blogArticles.find(article => article.id === parseInt(id));
};

export const getPublishedArticles = () => {
  return blogArticles.filter(article => article.published);
};

export const getFeaturedArticles = () => {
  return blogArticles.filter(article => article.featured && article.published);
};

export const getArticlesByCategory = (category) => {
  return blogArticles.filter(article => article.category === category && article.published);
};

export const getRelatedArticles = (currentArticle, limit = 3) => {
  return blogArticles
    .filter(article => 
      article.id !== currentArticle.id && 
      article.published &&
      (article.category === currentArticle.category || 
       article.tags.some(tag => currentArticle.tags.includes(tag)))
    )
    .slice(0, limit);
};

export const getCategories = () => {
  const categories = [...new Set(blogArticles.map(article => article.category))];
  return categories.map(category => ({
    name: category,
    count: getArticlesByCategory(category).length,
    icon: getCategoryIcon(category)
  }));
};

const getCategoryIcon = (category) => {
  const iconMap = {
    'Entrepreneurship': 'Rocket',
    'Personal Growth': 'TrendingUp',
    'Writing & Books': 'FileText',
    'Leadership': 'Crown'
  };
  return iconMap[category] || 'BookOpen';
};

// Function to add a new article (for easy publishing)
export const addArticle = (articleData) => {
  const newId = Math.max(...blogArticles.map(a => a.id)) + 1;
  const newArticle = {
    id: newId,
    slug: articleData.slug || generateSlug(articleData.title),
    published: true,
    views: 0,
    likes: 0,
    featured: false,
    ...articleData
  };
  blogArticles.push(newArticle);
  return newArticle;
};

// Function to generate URL-friendly slug
const generateSlug = (title) => {
  return title
    .toLowerCase()
    .replace(/[^a-z0-9\s-]/g, '')
    .replace(/\s+/g, '-')
    .replace(/-+/g, '-')
    .trim();
};

// Function to update article
export const updateArticle = (id, updates) => {
  const index = blogArticles.findIndex(article => article.id === id);
  if (index !== -1) {
    blogArticles[index] = { ...blogArticles[index], ...updates };
    return blogArticles[index];
  }
  return null;
};

// Function to delete article
export const deleteArticle = (id) => {
  const index = blogArticles.findIndex(article => article.id === id);
  if (index !== -1) {
    return blogArticles.splice(index, 1)[0];
  }
  return null;
};
